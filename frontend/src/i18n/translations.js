export const translateText = (language, key, fallback, variables = {}) => {
  const langMap = translations[language] || translations['en-US'];
  const defaultMap = translations['en-US'];
  let template = langMap[key] ?? defaultMap[key] ?? fallback ?? key;

  if (!template) {
    return '';
  }

  let translated = template;
  Object.keys(variables).forEach(variableKey => {
    const value = variables[variableKey];
    const regex = new RegExp(`{{\\s*${variableKey}\\s*}}`, 'g');
    translated = translated.replace(regex, value);
  });

  return translated;
};

export const getLanguageDisplayName = (language, { native = false } = {}) => {
  if (native) {
    return nativeLanguageNames[language] || nativeLanguageNames['en-US'];
  }
  return languageNames[language] || languageNames['en-US'];
};

